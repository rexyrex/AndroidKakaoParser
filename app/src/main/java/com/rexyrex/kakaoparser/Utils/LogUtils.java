package com.rexyrex.kakaoparser.Utils;

import android.util.Log;


//Custom Log Class
public class LogUtils {
    public static void e(String msg){
        if(true){
            Log.e("REXYREX", msg);
        }
    }

    public static void i(String msg){
        if(true){
            Log.i("REXYREX", msg);
        }
    }
}
